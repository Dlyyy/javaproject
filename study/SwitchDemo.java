class SwitchDemo{
	public static void main(String[] args){
		int x = 1 ;
		switch(x){
			case 1 :
				System.out.println("x");
				break ;
			case 2 :
				System.out.println("xx");
				break ;
			case 3 :
				System.out.println("xxx");
				break ;
			default:
				System.out.println("x?");
				break ;
		}
	}
}